package oca.ch02.operators;

public class OperatorModulo {
	
	public static void main(String[] args) {
		double a1 = 10.0;
		double a2 = 2.0;
		System.out.println(a1 / a2);//5.0
		double a3 = 3.0;
		System.out.println(a1 / a3);//3.3333333333333335
		System.out.println(a1 % a3);//1.0
		double a25 = 2.5;
		System.out.println(a1 % a25);//0.0 
	}

}
